package GetterandSetterinheritance;

public class MyDateParentClassinGS {

	/*
	 * int dd,mm,yy;
	 * 
	 * void setDate(int dd,int mm,int yy) { this.dd=dd; this.mm=mm; this.yy=yy;
	 */
	
	private int dd;
	private int mm;
	private int yy;
	
	public void setDd(int dd)
	{
		this.dd=dd;
	}
	public int getDd()
	{
		return dd;
	}
	public void setMm(int mm)
	{
		this.mm=mm;
	}
	public int getMm()
	{
		return mm;
	}
	public void setYy(int yy)
	{
		this.yy=yy;
	}
	public int getYy()
	{
		return yy;
	}
	
	
}
